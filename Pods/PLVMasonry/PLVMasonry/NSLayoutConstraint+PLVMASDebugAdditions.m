#import "NSLayoutConstraint+PLVMASDebugAdditions.h"
#import "PLVMASConstraint.h"
#import "PLVMASLayoutConstraint.h"

@implementation NSLayoutConstraint (PLVMASDebugAdditions)

#pragma mark - description maps

+ (NSDictionary *)plv_layoutRelationDescriptionsByValue {
    static dispatch_once_t once;
    static NSDictionary *descriptionMap;
    dispatch_once(&once, ^{
        descriptionMap = @{
            @(NSLayoutRelationEqual)                : @"==",
            @(NSLayoutRelationGreaterThanOrEqual)   : @">=",
            @(NSLayoutRelationLessThanOrEqual)      : @"<=",
        };
    });
    return descriptionMap;
}

