        };
    
    });
    return descriptionMap;
}


+ (NSDictionary *)plv_layoutPriorityDescriptionsByValue {
    static dispatch_once_t once;
    static NSDictionary *descriptionMap;
    dispatch_once(&once, ^{
#if TARGET_OS_IPHONE || TARGET_OS_TV
        descriptionMap = @{
            @(PLVMASLayoutPriorityDefaultHigh)      : @"high",
            @(PLVMASLayoutPriorityDefaultLow)       : @"low",
            @(PLVMASLayoutPriorityDefaultMedium)    : @"medium",
            @(PLVMASLayoutPriorityRequired)         : @"required",
            @(PLVMASLayoutPriorityFittingSizeLevel) : @"fitting size",
        };
#elif TARGET_OS_MAC
        descriptionMap = @{
            @(PLVMASLayoutPriorityDefaultHigh)                 : @"high",
            @(PLVMASLayoutPriorityDragThatCanResizeWindow)     : @"drag can resize window",
            @(PLVMASLayoutPriorityDefaultMedium)               : @"medium",
            @(PLVMASLayoutPriorityWindowSizeStayPut)           : @"window size stay put",
            @(PLVMASLayoutPriorityDragThatCannotResizeWindow)  : @"drag cannot resize window",
            @(PLVMASLayoutPriorityDefaultLow)                  : @"low",
            @(PLVMASLayoutPriorityFittingSizeCompression)      : @"fitting size",
            @(PLVMASLayoutPriorityRequired)                    : @"required",
        };
#endif
    });
    return descriptionMap;
}

#pragma mark - description override

+ (NSString *)descriptionForObject:(id)obj {
    if ([obj respondsToSelector:@selector(plv_key)] && [obj plv_key]) {
        return [NSString stringWithFormat:@"%@:%@", [obj class], [obj plv_key]];
    }
    return [NSString stringWithFormat:@"%@:%p", [obj class], obj];
}

- (NSString *)description {
    NSMutableString *description = [[NSMutableString alloc] initWithString:@"<"];

    [description appendString:[self.class descriptionForObject:self]];

    [description appendFormat:@" %@", [self.class descriptionForObject:self.firstItem]];
    if (self.firstAttribute != NSLayoutAttributeNotAnAttribute) {
        [description appendFormat:@".%@", self.class.plv_layoutAttributeDescriptionsByValue[@(self.firstAttribute)]];
    }

    [description appendFormat:@" %@", self.class.plv_layoutRelationDescriptionsByValue[@(self.relation)]];

    if (self.secondItem) {
        [description appendFormat:@" %@", [self.class descriptionForObject:self.secondItem]];
    }
    if (self.secondAttribute != NSLayoutAttributeNotAnAttribute) {
        [description appendFormat:@".%@", self.class.plv_layoutAttributeDescriptionsByValue[@(self.secondAttribute)]];
    }
    
    if (self.multiplier != 1) {
        [description appendFormat:@" * %g", self.multiplier];
    }
    
    if (self.secondAttribute == NSLayoutAttributeNotAnAttribute) {
        [description appendFormat:@" %g", self.constant];
    } else {
        if (self.constant) {
            [description appendFormat:@" %@ %g", (self.constant < 0 ? @"-" : @"+"), ABS(self.constant)];
        }
    }

    if (self.priority != PLVMASLayoutPriorityRequired) {
        [description appendFormat:@" ^%@", self.class.plv_layoutPriorityDescriptionsByValue[@(self.priority)] ?: [NSNumber numberWithDouble:self.priority]];
    }

    [description appendString:@">"];
    return description;
}

@end
